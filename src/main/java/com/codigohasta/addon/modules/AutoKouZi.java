    @Override
    public void onActivate() {
        if (mc.player == null) {
            toggle();
            return;
        }

        // 私聊模式校验
        if (whisperMode.get() && !randomWhisper.get() && targetPlayer.get().trim().isEmpty()) {
            ChatUtils.info("错误：私聊模式需要填写目标玩家");
            toggle();
            return;
        }
        
        lastTime = System.currentTimeMillis();
        ChatUtils.info("自动扣字已启动");
    }

    @Override
    public void onDeactivate() {
        ChatUtils.info("自动扣字已停止");
    }

    @EventHandler
    private void onTick(TickEvent.Pre event) {
        if (mc.player == null || mc.getNetworkHandler() == null) return;
        
        // 检查时间间隔
        if (System.currentTimeMillis() - lastTime < delay.get() * 1000) {
            return;
        }

        lastTime = System.currentTimeMillis();

        try {
            // 获取消息
            String msg = getRandomMessage();

            // 添加随机字符串
            if (randomLength.get() > 0) {
                String randomStr = generateRandomString(randomLength.get());
                if (!randomStr.isEmpty()) {
                    msg = msg + " " + randomStr;
                }
            }

            // 构建最终消息
            String finalContent;

            if (whisperMode.get()) {
                String target = getTargetPlayer();
                if (target == null) {
                    ChatUtils.warning("无法获取目标玩家，跳过本次发送。");
                    return;
                }
                finalContent = "/" + whisperCmd.get().trim() + " " + target + " " + msg;
            } else {
                finalContent = msg;
            }

            // 发送消息
            sendMessage(finalContent);

        } catch (Exception e) {
            ChatUtils.error("发送失败：" + e.getMessage());
        }
    }

    private String getRandomMessage() {
        if (messages.isEmpty()) {
            return defaultMessage.get();
        }
        return messages.get(random.nextInt(messages.size()));
    }

    private String getTargetPlayer() {
        if (randomWhisper.get()) {
            // 随机选择在线玩家
            Collection<PlayerListEntry> players = mc.getNetworkHandler().getPlayerList();
            ArrayList<PlayerListEntry> list = new ArrayList<>(players);

            if (list.isEmpty()) {
                return null;
            }

            PlayerListEntry playerListEntry = list.get(random.nextInt(list.size()));

            // 检查是否排除自己
            if (checkSelf.get()) {
                int attempts = 0;
                while (Objects.equals(playerListEntry.getProfile().name(), mc.player.getName().getString())) {
                    if (attempts > 50) {
                        return null;
                    }
                    attempts++;
                    playerListEntry = list.get(random.nextInt(list.size()));
                }
            }

            return playerListEntry.getProfile().name();
        } else {
            // 使用固定的目标玩家
            return targetPlayer.get().trim();
        }
    }

    private void sendMessage(String content) {
        if (commandMode.get() || content.startsWith("/")) {
            // 发送命令 (去掉开头的 / 因为 sendChatCommand 不需要)
            String cmd = content.startsWith("/") ? content.substring(1) : content;
            mc.getNetworkHandler().sendChatCommand(cmd);
        } else {
            // 发送普通消息
            mc.getNetworkHandler().sendChatMessage(content);
        }
    }

    private String generateRandomString(int length) {
        if (length <= 0) return "";

        StringBuilder sb = new StringBuilder(length);
        for (int i = 0; i < length; i++) {
            int index = random.nextInt(CHARACTERS.length());
            sb.append(CHARACTERS.charAt(index));
        }
        return sb.toString();
    }
